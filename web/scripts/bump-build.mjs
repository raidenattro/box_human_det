import { copyFileSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const webRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const repoRoot = join(webRoot, '..');
const versionPath = join(repoRoot, 'version.json');
const webVersionPath = join(webRoot, 'version.json');
const infoPath = join(webRoot, 'src', 'build-info.js');

const v = JSON.parse(readFileSync(versionPath, 'utf8'));
const buildNum = Number(v.build);
const version = `v${v.major}.${v.minor}.${v.patch}.build${buildNum}`;

v.build = buildNum + 1;
const versionJson = `${JSON.stringify(v, null, 2)}\n`;
writeFileSync(versionPath, versionJson);
writeFileSync(webVersionPath, versionJson);
writeFileSync(
  infoPath,
  `// Generated by scripts/bump-build.mjs — do not edit by hand\nexport const APP_VERSION = '${version}';\n`,
);

const assetsDir = join(webRoot, 'src', 'assets');
mkdirSync(assetsDir, { recursive: true });
copyFileSync(join(webRoot, 'public', 'logo.svg'), join(assetsDir, 'logo.svg'));

console.log(`[version] ${version}`);
